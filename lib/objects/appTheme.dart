import 'dart:ui';

class AppTheme {
  final String name;
  final Color cardColor;
  final List<Color> background;
  final Color textColor;
  final Color secondaryTextColor;
  final Color headlineColor;
  final Color primaryColor;
  final Color secondaryColor;
  

  AppTheme({
    this.name,
    this.cardColor,
    this.background,
    this.textColor,
    this.secondaryTextColor,
    this.headlineColor,
    this.primaryColor,
    this.secondaryColor,
  });
}